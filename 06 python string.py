#Python Strings
x="String literals in python are surrounded by either single quotation marks, or double quotation marks."
print(x)
# Strings are Arrays
#Slicing
print(x[0:2])
print(x[11:])
print(x[::2])

print(x[::-1])

#negative slicing
print(x[-5:-2])
# length of string

print(len(x))

# upper and lower
print(x.lower())
print(x.upper())
print(x.replace("String","Mukund briaaar"))